# Given an integer n, find if n is positive, negative or 0.
# If n is positive, print "Positive"
# If n is negative, print "Negative"
# And if n is equal to 0, print "Zero".


n = int(input())

if n > 0:
    print("Positive")
elif n == 0:
    print("Zero")
else:
    print("Negative")
